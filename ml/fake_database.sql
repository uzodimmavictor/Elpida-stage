CREATE EXTENSION IF NOT EXISTS pgcrypto;

DROP TABLE IF EXISTS panier_ligne_option;
DROP TABLE IF EXISTS panier_lignes;
DROP TABLE IF EXISTS paniers;
DROP TYPE IF EXISTS panier_ligne_type;
DROP TYPE IF EXISTS panier_status;

CREATE TYPE panier_status AS ENUM ('draft', 'confirmed', 'cancelled');
CREATE TYPE panier_ligne_type AS ENUM ('produit', 'option');

CREATE TABLE paniers (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    enseigne_id UUID NOT NULL,
    client_id UUID NULL,
    status panier_status NOT NULL,
    montant_total DECIMAL(10,2),
    remise DECIMAL(10,2),
    created_at TIMESTAMP DEFAULT now(),
    confirmed_at TIMESTAMP
);

CREATE TABLE panier_lignes (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    panier_id UUID REFERENCES paniers(id) ON DELETE CASCADE,
    parent_id UUID REFERENCES panier_lignes(id),
    type panier_ligne_type NOT NULL,
    produit_id UUID NOT NULL,
    groupe_option_id UUID,
    quantite INT NOT NULL CHECK (quantite > 0),
    prix_unitaire DECIMAL(10,2) NOT NULL,
    montant NUMERIC(12,2) NOT NULL,
    remise DECIMAL(10,2) DEFAULT 0.0,
    tva DECIMAL DEFAULT 0.0
);

CREATE TABLE panier_ligne_option (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    panier_id UUID NOT NULL REFERENCES panier_lignes(id) ON DELETE CASCADE,
    produit_id UUID,
    groupe_option_id UUID,
    quantite NUMERIC(12,3) DEFAULT 1,
    supplement_unitaire NUMERIC(12,2) DEFAULT 0,
    montant_supplement NUMERIC(12,2) DEFAULT 0
);

